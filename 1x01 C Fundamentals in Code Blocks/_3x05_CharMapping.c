void _3x05_CharMapping() {

	printf("==============================================================================\n");
	printf("\n");
	printf("                          Character Mapping                                   \n");
	printf("\n");
	printf("==============================================================================\n");

	for (int i = 0; i < 256; i++) {
		printf("%i - %c\t", i, i);
		if (i % 8 == 0)
			printf("\n");
	}
	printf("\n");
	printf("\n");
	printf("==============================================================================\n");
	system("Pause");

}
